# 显示十进制数,将浮点数转换为十进制数
print("%d %d" % (12, 12.3))

# 设定十进制数的显示宽度
print("%6d %6d" % (12, 12.3))

# 设定十进制数的显示宽度和对齐方式
print("%-6d" % (12))

# 以浮点数方式显示
print("%f" % (100))

# 以浮点数方式显示,并设置其宽度和小数位数
print("%6.2f" % (100))

# 以科学计数法表示
print("%e" % (100))

# 显示字符串和整数,并分别设置其宽度
print("%10s is %-3d years old" % ("Rose", 18))
